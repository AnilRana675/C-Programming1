//WAP to declare the character 'a' in variable x and print x increment the value of x and print the x as a character

#include <stdio.h>

int main() {
    // Declare a character variable x and initialize it with 'a'
    char x = 'a';

    // Print the initial value of x as a character
    printf("Initial value of x: %c\n", x);

    // Increment the value of x using the increment operator (++)
    x++;

    // Print the incremented value of x as a character
    printf("Value of x after increment: %c\n", x);

    return 0;
}
