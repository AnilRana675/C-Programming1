#include<stdio.h>
int main(){
    int a=10,b=4,res;
    printf("a is %d and b is %d \n",a,b);
    res=a+b;//Addition
    printf("a+b is %d\n",res);
    res=a-b;//subtraction
    printf("a-b is %d\n",res);
    res=a*b;//Multiplication
    printf("a*b is %d\n",res);
    res=a/b;//Division
    printf("a/b is %d\n",res);
    res=a%b;//Modulus
    printf("a%b is %d\n",res);
    return 0;
}
